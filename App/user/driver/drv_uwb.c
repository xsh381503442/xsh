/*
*函数: 发送数据
*输入: ・CO_Data   	CO浓度数据,单位 ppm，整数  
		heartrate	心率值
*返回值:无
*备注 
*/
void SendData( uint16_t CO_Data, uint8_t  heartrate )
{
	uint16_t	crc = 0;
	uint8_t		i;
	
	am_hal_gpio_out_bit_set( BSP_GPIO_UWB_WAKEUP );
//	bsp_uart_init(0,g_UWB_UartConfig,g_UWB_Rx_Buffer,g_UWB_Tx_Buffer,UWB_BUFFER_SIZE);
	
	sendbuff[0] = 0x59;    	//SOF
	sendbuff[1] = 10;			//length
	sendbuff[2] = 0x80;		//command
	sendbuff[3] = (uint8_t)(CO_Data & 0x00FF);	//
	sendbuff[4] = (uint8_t)(CO_Data >> 8 );
	sendbuff[5] = (uint8_t)(heartrate & 0x00FF);
	sendbuff[6] = (uint8_t)(heartrate >> 8 );
	crc = Get_Crc16( sendbuff,7 );
	sendbuff[7] = (uint8_t)(crc & 0x00FF);
	sendbuff[8] = (uint8_t)(crc >> 8 );
	sendbuff[9] = 0x47;		//EOF
	
	for(i = 0;i < 10;i++)
	{
		am_hal_uart_char_transmit_buffered(0, sendbuff[i]);	
		while ( am_hal_uart_flags_get(0) & (AM_HAL_UART_FR_BUSY | AM_HAL_UART_FR_TX_FULL));
	}
}


/*uwb初始化 --对串口，硬件使能脚，输出脚配置*/
void drv_uwb_init(void)
{
	DRV_HEARTRATE_LOG_PRINTF("[drv_uwb]:drv_uwb_init!\n");
    
    tca6408a_gpio_out_bit_set(BSP_GPIO_UWB_POWER);    
  
	
    am_hal_gpio_pin_config(BSP_GPIO_UWB_WAKEUP,AM_HAL_GPIO_OUTPUT | AM_HAL_GPIO_PULLUP );
	am_hal_gpio_out_bit_set( BSP_GPIO_UWB_WAKEUP );
    
    am_hal_gpio_pin_config(BSP_GPIO_UWB_BUSY,AM_HAL_GPIO_INPUT  );

    
    am_hal_gpio_pin_config(BSP_GPIO_UWB_SOS,AM_HAL_GPIO_OUTPUT | AM_HAL_GPIO_PULLUP );
	am_hal_gpio_out_bit_set( BSP_GPIO_UWB_SOS );
    
    bsp_uart_init(0,g_UWB_UartConfig,g_UWB_Rx_Buffer,g_UWB_Tx_Buffer,UWB_BUFFER_SIZE);
    


}
void drv_uwb_uninit(void)
{
    bsp_uart_disable(0);
	am_hal_gpio_pin_config(BSP_GPIO_UWB_WAKEUP,AM_HAL_PIN_DISABLE);
	am_hal_gpio_pin_config( BSP_GPIO_UWB_SOS, AM_HAL_PIN_DISABLE);
    am_hal_gpio_pin_config(BSP_GPIO_UWB_BUSY,AM_HAL_PIN_DISABLE);

	tca6408a_gpio_out_bit_clear( BSP_GPIO_UWB_POWER );
		
	DRV_HEARTRATE_LOG_PRINTF("[drv_uwb]:drv_uwb_uninit!\n");
}





