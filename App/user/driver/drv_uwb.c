uint16_t  Get_Crc16(uint8_t *pucFrame,uint8_t usLen)
{
    uint8_t ucCRCHi = 0xFF;
    uint8_t ucCRCLo = 0xFF;
    int iIndex;
    while( usLen-- )
    {
        iIndex = ucCRCLo ^ *( pucFrame++ );
        ucCRCLo = (uint8_t)( ucCRCHi ^ aucCRCHi[iIndex] );
        ucCRCHi = aucCRCLo[iIndex];
    }
    return (uint16_t)( ucCRCHi << 8 | ucCRCLo );
}

/*
*函数: 设置标签发送信号周期
*输入: ・period   周期,单位0.1s，整数  例：1表示间隔0.1s发送，10表示间隔1s发送
		
*返回值:无
*备注 
*/
void SetPeriod( uint8_t period )
{
	uint16_t	crc = 0;
	uint8_t		i;
	
	am_hal_gpio_out_bit_set( BSP_GPIO_UWB_WAKEUP );
//	bsp_uart_init(0,g_UWB_UartConfig,g_UWB_Rx_Buffer,g_UWB_Tx_Buffer,UWB_BUFFER_SIZE);
	
	while(am_hal_gpio_input_bit_read(BSP_GPIO_UWB_BUSY) != 0);
	
	sendbuff[0] = 0x59;    	//SOF
	sendbuff[1] = 7;			//length
	sendbuff[2] = 0x81;		//command
	sendbuff[3] = period;	//period
	crc = Get_Crc16( sendbuff,4 );
	sendbuff[4] = (uint8_t)(crc & 0x00FF);
	sendbuff[5] = (uint8_t)(crc >> 8 );
	sendbuff[6] = 0x47;		//EOF
	
	for(i = 0;i < 7;i++)
	{
		am_hal_uart_char_transmit_buffered(0, sendbuff[i]);	
		
		while ( am_hal_uart_flags_get(0) & (AM_HAL_UART_FR_BUSY | AM_HAL_UART_FR_TX_FULL));
	}
}

